#ifndef _SMSC_UTIL_PTRLESS_H
#define _SMSC_UTIL_PTRLESS_H

namespace smsc {
namespace util {

struct PtrLess
{
    template < class T > bool operator () ( const T* t1, const T* t2 ) const
    {
        return (*t1 < *t2);
    }
    template < class T > bool operator () ( T* t1, T* t2 ) const
    {
        return (*t1 < *t2);
    }
};

}
}

#endif /* !_SCAG_UTIL_PTRLESS_H */
