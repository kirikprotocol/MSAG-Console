generate ()
{
  mkdir -p $DIR; cd $DIR; rm -rf *.[ch]; echo Generate in `pwd`
  $ASN1C $ASN1COPT $SPECLIST
  find . -type l  -exec rm {} \;
  rm Makefile.am.sample
#  echo OUTLIST=$OUTLIST
  rm $OUTLIST
  OUTLIST=`ls *.[ch]`
}
